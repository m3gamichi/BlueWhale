def General_Error(e):
    """Zentrale Fehlerausgabe, egal ob Exception oder Custom-String."""
    if isinstance(e, Exception):
        msg = ERROR_MESSAGES.get(type(e), f"Unknown Error: {e}")
    elif isinstance(e, str):
        msg = ERROR_MESSAGES.get(e, f"Unknown Custom Error: {e}")
    else:
        msg = f"Unknown Error Type: {e}"

    print(
        f"{Pre("x",color.RED)} {msg}\n",
        " " * 9,
        f"{ERROR} {color.WHITE}{e}", color.RESET,
    )
    Continue()


def ChoiceUserAgent():
    file_user_agent = os.path.join(scripts_path, "data", "UserAgent.txt")

    with open(file_user_agent, "r", encoding="utf-8") as file:
        lines = file.readlines()

    if lines:
        user_agent = random.choice(lines).strip()
    else:
        user_agent = "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/109.0.5414.120 Safari/537.36"

    return user_agent


def CheckWebhook(webhook):
    try:
        response = requests.get(webhook)
        if response.status_code == 200 or response.status_code == "200":
            return True
        else:
            return False
    except:
        return None







